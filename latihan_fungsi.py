'''Fungsi dengan parameter'''
'''def_nama_fungsi(p1, p2)'''

def say_hi(nama):
  print("Hallo nama saya", nama)

say_hi("Joko")
say_hi("org")
say_hi("ohueaw")

print("\n<..............................>\n")

'''Buatlah fungsi dengan 3 parameter'''

def angka(nomor1, nomor2, nomor3):
  print("nomor anda:", nomor1)
  print("nomor baru:", nomor2)
  print("nomor lama:", nomor3)

angka("6", "9", "78")


